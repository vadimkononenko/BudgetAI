def generate_variations(templates, keywords, transaction_type):
    """–ì–µ–Ω–µ—Ä—É—î –≤–∞—Ä—ñ–∞—Ü—ñ—ó —Ñ—Ä–∞–∑ –¥–ª—è –∫–æ–∂–Ω–æ—ó –∫–∞—Ç–µ–≥–æ—Ä—ñ—ó"""
    result = []

    for category, keyword_list in keywords.items():
        for keyword in keyword_list:
            # –î–æ–¥–∞—î–º–æ –æ—Ä–∏–≥—ñ–Ω–∞–ª—å–Ω–∏–π –∫–ª—é—á (–±–µ–∑ —à–∞–±–ª–æ–Ω—É)
            result.append({
                'text': f"{transaction_type}: {keyword}",
                'label': category
            })

            # –î–æ–¥–∞—î–º–æ –≤–∞—Ä—ñ–∞—Ü—ñ—ó –∑ —à–∞–±–ª–æ–Ω—ñ–≤
            for template in templates[:3]:  # –ë–µ—Ä–µ–º–æ –ø–µ—Ä—à—ñ 3 —à–∞–±–ª–æ–Ω–∏ –¥–ª—è —Ä—ñ–∑–Ω–æ–º–∞–Ω—ñ—Ç–Ω–æ—Å—Ç—ñ
                variation = template.format(item=keyword)
                result.append({
                    'text': f"{transaction_type}: {variation}",
                    'label': category
                })

    return result

